function uuid() {
  return '' + Math.random().toString(36).substr(2, 9);
}

/**
 * @param {Array} a items An array containing the items.
 */
function shuffle(a) {
  for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export default {
  uuid,
  shuffle
}